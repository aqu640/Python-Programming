num1 = float (input("plz input the first number: "))  # 數字一
op = input("plz input the operator")                  # 輸入運算符號
num2 = float (input("plz input the sacond number: ")) # 數字二

if op == "+" :
    print(num1+num2)
elif op == "-" :
    print(num1-num2)
elif op == "*" :
    print(num1*num2)
elif op == "/" :
    print(num1/num2)
        
